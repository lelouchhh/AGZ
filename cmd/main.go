package main

import (
	"AGZ/pkg/repository"
	"AGZ/pkg/service"
	"AGZ/pkg/structures"
	"context"
	"os"
	"os/signal"
	"syscall"

	"AGZ/pkg/handler"
	"github.com/joho/godotenv"
	_ "github.com/lib/pq"
	"github.com/sirupsen/logrus"
	"github.com/spf13/viper"
)

